<u><b>How do the dice games work?</b></u><br><br>
The games are Crowns and Tops. You can wager money which is either won or lost at the end of the round. The more players,
the greater the winnings but the lower the chances. The games are essentially the same, except Crowns uses symbols while Tops
uses regular numbered dice and includes straights.
<br>Crown=Six, Rose=Five, Sword=Four, Star=Three, Cups=Two, Rods=One.<br><br>
In the games you are trying for combinations, listed here in order of worth:<br><br>
5 of a kind<br>
4 of a kind<br>
Full house<br>
Straight (Tops only)<br>
3 of a kind<br>
2 pair<br>
1 pair
