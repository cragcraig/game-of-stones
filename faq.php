<u><b>Ok, I Joined. Now what do I do?</b></u><br><br>
These are the basic points to the game:<br>
1. Fighting<br>
2. Getting money<br>
3. Buying items<br>
4. Using 1-3 to make your character better at doing 1-2.
<br><br><br>

<u><b>How do I get money?</b></u><br><br>
Selling items and resources. You can sell items from your inventory either instantly (lower profit) or on the market,
which takes longer but gets you more money. Selling in the market allows other users to search for and purchase your item from you at the price you set.
Once put up for sale in the market the item will dissapear from your inventory and appear in your shop. Remove it from
your shop and it will go back to your inventory.
<br><br><br>

<u><b>But how do resources work?</b></u><br><br>
Ah ha! I was just getting to that. <br>The best way to increase your money is by selling and buying commodities.
The worth of commodities vary as time goes on, and each has a different characteristic. For example, the worth of goods stays
fairly level, but the worth of livestock has fairly large jumps with stable periods between. Each commodity is unique.<br>
Buying low and selling high is the name of the game. Actually, it isn't, but it could have been.
<br><br><br>

<u><b>Fighting!</b></u><br><br>
Fighting is simple! Go to a character's Bio screen and the word [battle] should be somewhere
to the lower right of the character's avatar. Click on it.<br>
The battle will now unfold before your very eyes! The win (or loss) will then go onto your permanent record, as well as adding
to your clan's rank. If you won you may have gold to pick up. If you lost, perhaps you should read up on the next two parts of this FAQ.
Battles only count towards your wins if the other character is within a few levels of you.<br>
Alternatively, you can click the "Find Battle" button on the top menu and you will randomly fight an enemy character within a few levels and find items.
<br><br><br>

<u><b>Skills</b></u><br><br>
Each character type has six unique skills. You may choose three to use at any one time. One will read 50%, the other two 25%.
This corresponds to their chances of being used in battle. As you use a skill it will gain levels. Most likely the skill
often in the 50% slot will gain levels more quickly than your other skills. You may want to try switching them around because
the higher the level of a skill the stronger it is and the more passive benefits it gives to your character, even when it is not
assigned.
<br><br><br>

<u><b>Items</b></u><br><br>
Items can be equipped or sold from a character's inventory. There are different weapon types, prefixes, and suffixes.
Each adds different damage, types of damage, and effects to your attacks. In the future there will be more equippable slots.
<br><br><br>

<u><b>Pure, Evil, or Neutral?</b></u><br><br>
This depends on your choice of skills. A fairly balanced character will be neutral, a character generally using more
Shadow affiliated skills will be evil and a character using Light affiliated skills will be Pure. This shows up both
in a character's bio and determines the color of a character's name in the World Lists. It can also be used to limit
a character's access to Clans.
<br><br><br>

<u><b>Clans? What's the point?</b></u><br><br>
Clans allow you to chat with fellow players as well as wage war on other groups of characters.
After joining a clan the World List will have an axe next to characters from enemy Clans and a horn next to memebers
of allied Clans. These enemies and allies are chosen by the clan leader. The clan leader may also choose only to allow
characters of certain affiliations to join and/or require an invitation. The clan leader may also kick troublesome clan
members.<br><br>
Any clan memeber that is inactive for more than twenty days will be ejected from the clan. Any clan leader who is inactive
for more than 5 days will loose leadership and it will pass to the character with the next highest number of wins in the clan.
