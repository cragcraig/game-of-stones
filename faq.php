<u><b>What is GoS?</b></u><br><br>
A Game of Stones is a browser based MMORPG. A big imaginary world where people create characters and then do stuff with them.
In GoS you create a character as one of three general types then develop the character in a world based off The Wheel of Time
book series by Robert Jordan. As you may have guessed, I am a fan of the series myself (I've been thinking the whole game thing
is sort of a dead giveaway for the astute reader).
<br><br><br>

